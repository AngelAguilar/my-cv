import UIKit

extension UIImageView {
    
    public func transition(toImage image: UIImage?) {
        UIView.transition(with: self, duration: 0.3,
                          options: [.transitionCrossDissolve],
                          animations: {
                            self.image = image
                          },
                          completion: nil)
    }
}
